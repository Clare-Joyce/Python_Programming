#enter a number btn 1 and 7 and get the corresponding day of the week.
num = int(input("Enter a number: "))
if num == 1:
	print("Monday")
elif num == 2:
	print("Tuesday")
elif num == 3:
	print("Wednesay")
elif num == 4:
	print("Thursday")
elif num == 5:
	print("Friday")
elif num == 6:
	print("Saturday")
elif num == 7:
	print("Sunday")
else:
	print("Invalid! please change the number. ")
	