#displaying corresponding roman numberals for numbers
#prompt a user to enter a number btn 1 and 10
num = int(input("Enter a number: "))
if num == 1:
	print(num, "I")
elif num == 2:
	print(num, "II")
elif num == 3:
	print(num, "III")
elif num == 4:
	print(num, "IV")
elif num == 5:
	print(num, "V")
elif num == 6:
	print(num, "VI")
elif num == 7:
	print(num, "VII")
elif num == 8:
	print(num, "VIII")
elif num == 9:
	print(num, "IX")
elif num == 10:
	print(num, "X")
else:
	print("Invalid! please change the number. ")
